package com.example.meetfurryapplication.Model;

import java.util.Date;

public class Comment {

        private String comment, userId, username;
        private Date time;

    public String getComment() {
        return comment;
    }

    public String getUserId() {
        return userId;
    }

    public String getUsername() {
        return username;
    }

    public Date getTime() {
        return time;
    }
}
